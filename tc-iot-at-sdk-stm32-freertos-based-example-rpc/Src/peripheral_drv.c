HAL_StatusTypeDef OledClear(uint8_t fill_Data)
{
	uint8_t m;
	uint8_t	n;
	HAL_StatusTypeDef Ret;
	

	for(m = 0;m < CHAR_WID_16_ADD; m++)
	{
		Ret  = OledWriteOnI2c(CMD_TYPE, 0xb0 + m);		//page0-page7
		Ret |= OledWriteOnI2c(CMD_TYPE, 0x01);		    //low column start address
		Ret |= OledWriteOnI2c(CMD_TYPE, 0x10);		    //high column start address
		
		for(n = 0; n < OLED_MAX_COL; n++)
		{
			Ret |= OledWriteOnI2c(DATA_TYPE, fill_Data);
			if(HAL_OK != Ret)
			{
				return Ret;
			}
		}		
	}


	return Ret;
}

