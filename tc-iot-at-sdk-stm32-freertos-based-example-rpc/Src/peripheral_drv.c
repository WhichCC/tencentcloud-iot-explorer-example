HAL_StatusTypeDef HumAndTempRead(eHT_Select_type eSelect, float *pValue) 
{	
	uint8_t DataBuff[4] = {0};
	uint8_t CmdBuff[2] = {0};
	uint16_t rawData;
	HAL_StatusTypeDef Ret;
	
	if(HUM_READ == eSelect)
	{	
		CmdBuff[0] = CMD_READ_HUM;
		Ret = HAL_I2C_Master_Transmit(g_HtI2C, HTU_I2C_ADDRESS, CmdBuff, 1, I2C_TIME_OUT);
		Ret |= HAL_I2C_Master_Receive(g_HtI2C, HTU_I2C_ADDRESS, DataBuff, 3, I2C_TIME_OUT);
		rawData = ((DataBuff[0] << 8) | DataBuff[1])&0xFFFC;
		*pValue =  (rawData*125)/65536 - 6;
	}
	else
	{
		CmdBuff[0]  = CMD_READ_TEMP;
		Ret = HAL_I2C_Master_Transmit(g_HtI2C, HTU_I2C_ADDRESS, CmdBuff, 1, I2C_TIME_OUT);
		Ret |= HAL_I2C_Master_Receive(g_HtI2C, HTU_I2C_ADDRESS, DataBuff, 3, I2C_TIME_OUT);
		rawData = ((DataBuff[0] << 8) | DataBuff[1])&0xFFFC;
		*pValue =  (rawData*175.72)/65536 - 46.85;
	}

	return Ret;	
}
