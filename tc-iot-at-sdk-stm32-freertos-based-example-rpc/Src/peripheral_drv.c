static inline void CountForClk(uint32_t  count)
{
	volatile uint32_t i;
	
	for(i=0; i < count; i++);
}

/*************************************************

Function: GenOneClk

Description: generation one clock 
Input: void

Output:void

Return: none

*************************************************/
static void GenOneClk(void)
{
	GPIO_CIN_LOW;    
	CountForClk(RGB_CLK_CYCLE);
	GPIO_CIN_HIGH;  
	CountForClk(RGB_CLK_CYCLE);	
}
/*************************************************

Function: TakeAntiCode

Description: invert the grey value of the first two bits
Input: void

Output:void

Return: none

*************************************************/

static uint8_t TakeAntiCode(uint8_t data)
{
	uint8_t tmp = 0;

	tmp=((~data) & 0xC0)>>6; 
	return tmp;
}

static void RGBSendZeroFrame(void)
{
	uint8_t  i; 

	GPIO_DIN_LOW;

	for (i = 0; i < RGB_FRAME_BIT; i++)
	{
		GenOneClk();
	}
}

 
/*************************************************

Function: RGBFrameDataSend

Description: send one frame data
Input: void

Output:void

Return: none

*************************************************/

static void RGBFrameDataSend(uint32_t dx)
{
	uint8_t i;

	for (i=0; i < RGB_FRAME_BIT; i++)
	{
		if ((dx & RGB_FRAME_MSB) != 0)
		{
			GPIO_DIN_HIGH; 
		}
		else
		{
			GPIO_DIN_LOW;
		}

		dx <<= 1;
		GenOneClk();
	}
}

/*************************************************

Function: GrayDataSend

Description: send gray data by R,G,B value
Input: void

Output:void

Return: none

*************************************************/

void GrayDataSend(uint8_t r, uint8_t g, uint8_t b)
{
	uint32_t dx = 0;

	dx |= (uint32_t)0x03 << 30;             // The front of the two bits 1 is flag bits
	dx |= (uint32_t)TakeAntiCode(b) << 28;
	dx |= (uint32_t)TakeAntiCode(g) << 26;	
	dx |= (uint32_t)TakeAntiCode(r) << 24;

	dx |= (uint32_t)b << 16;
	dx |= (uint32_t)g << 8;
	dx |= r;

	RGBFrameDataSend(dx);

	RGBSendZeroFrame();
}

