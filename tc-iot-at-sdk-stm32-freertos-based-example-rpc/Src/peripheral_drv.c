static HAL_StatusTypeDef OledSetPos(unsigned char x, unsigned char y) 
{ 	
	HAL_StatusTypeDef Ret;
	
	Ret = OledWriteOnI2c(CMD_TYPE, 0xb0+y);
	Ret |= OledWriteOnI2c(CMD_TYPE, ((x&0xf0)>>4)|0x10);
	Ret |= OledWriteOnI2c(CMD_TYPE, (x&0x0f)); 

	return Ret;
} 

