static HAL_StatusTypeDef OledWriteOnI2c(uint8_t oled_cmd_first,uint8_t oled_cmd_second) 
{
	uint8_t SendData[2] = {0};

	SendData[0] = oled_cmd_first;
	SendData[1] = oled_cmd_second;
	
	return HAL_I2C_Master_Transmit(g_OledI2C, OLED_I2C_ADDRESS, SendData, 2, I2C_TIME_OUT);	
}

