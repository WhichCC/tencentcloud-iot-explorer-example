HAL_StatusTypeDef OledShowString(uint8_t x, uint8_t *pRow, char *chr, uint8_t Char_Size, bool IsClear)
{
	uint8_t pos = 0;
	HAL_StatusTypeDef Ret;
	
	if(IsClear == true || (*pRow > OLED_MAX_ROW))
	{
		*pRow = 0;
		OledClear(0);		
		HAL_Delay(10);
	}
	
	while (chr[pos] != '\0')
	{	

		if(HAL_OK != (Ret = OLED_ShowChar(x, *pRow, chr[pos], Char_Size)))
		{
			break;
		}
		
		if(Char_Size == CHAR_WID_16)
		{
			x += CHAR_WID_16_ADD;
		}
		else if(Char_Size == CHAR_WID_8)
		{
			x += CHAR_WID_8_ADD;
		}
		
		if(x > (OLED_MAX_COL - Char_Size))
		{
			x = 0;
			*pRow += 1;
		}

		pos++;
	}

	if(x > 0)
	{
		*pRow += 1;
	}

	return Ret;
}


